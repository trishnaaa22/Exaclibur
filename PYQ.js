        function showPapers() {
            var exam = document.getElementById("exam").value;
            var year = document.getElementById("year").value;
            var linksContainer = document.getElementById("paperLinks");
            var linksList = document.getElementById("linksList");

            linksList.innerHTML = ""; // Clear previous links

            if (exam && year) {
                let key = exam + "_" + year;
                if (paperLinksData[key]) {
                    linksContainer.style.display = "block";
                    paperLinksData[key].forEach(paper => {
                        var paperLink = document.createElement("a");
                        paperLink.href = paper.url;
                        paperLink.target = "_blank";
                        paperLink.className = "btn";
                        paperLink.innerText = paper.name;
                        linksList.appendChild(paperLink);
                    });
                } else {
                    linksContainer.style.display = "none";
                }
            } else {
                linksContainer.style.display = "none";
            }
        }
    